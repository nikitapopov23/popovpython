# Создайте базовый класс "Фигура" со свойствами "ширина" и "высота". От этого
# класса унаследуйте классы "Прямоугольник" и "Квадрат". Для класса "Квадрат"
# переопределите методы, связанные с вычислением площади и периметра.
class Фигура:
    def __init__(self, ширина, высота):
        self.ширина = ширина
        self.высота = высота

    def площадь(self):
        """Метод для вычисления площади фигуры. Будет переопределен в дочерних классах."""
        raise NotImplementedError("Метод 'площадь' должен быть переопределен в дочернем классе.")

    def периметр(self):
        """Метод для вычисления периметра фигуры. Будет переопределен в дочерних классах."""
        raise NotImplementedError("Метод 'периметр' должен быть переопределен в дочернем классе.")


class Прямоугольник(Фигура):
    def площадь(self):
        """Вычисление площади прямоугольника."""
        return self.ширина * self.высота

    def периметр(self):
        """Вычисление периметра прямоугольника."""
        return 2 * (self.ширина + self.высота)


class Квадрат(Фигура):
    def __init__(self, сторона):
        super().__init__(сторона, сторона)  

    def площадь(self):
        """Вычисление площади квадрата."""
        return self.ширина ** 2

    def периметр(self):
        """Вычисление периметра квадрата."""
        return 4 * self.ширина



